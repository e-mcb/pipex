#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while ((s1[i] == s2[i]) && (s1[i] != '\0') && (s1[i] != 0 || s2[i] != 0))
	{
		i++;
		if (i == n)
			return (s1[i - 1] - s2[i - 1]);
	}
	return (s1[i] - s2[i]);
}
